numbers = [64, 34, 25, 12, 22, 11, 90]
print("Original Numbers:", numbers)

numbers.sort()
print("Sorted Numbers:", numbers)

numbers.sort(reverse=True)
print("Reverse Sorted Numbers:", numbers)

numbers.reverse()
print("After Numbers:", numbers)

